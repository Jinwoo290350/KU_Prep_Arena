// ── DETERMINISTIC SEEDED RANDOM ─────────────────────────────────────────────

function seededRandom(seed: string): () => number {
  // Simple mulberry32-style hash from string
  let h = 0
  for (let i = 0; i < seed.length; i++) {
    h = Math.imul(31, h) + seed.charCodeAt(i) | 0
  }
  return function () {
    h |= 0
    h = h + 0x6D2B79F5 | 0
    let t = Math.imul(h ^ h >>> 15, 1 | h)
    t = t + Math.imul(t ^ t >>> 7, 61 | t) ^ t
    return ((t ^ t >>> 14) >>> 0) / 4294967296
  }
}

// ── DAILY CARDS ──────────────────────────────────────────────────────────────

export function getDailyCards(dateStr: string): {
  overall: { card: TarotCard; reversed: boolean }
  study: { card: TarotCard; reversed: boolean }
  finance: { card: TarotCard; reversed: boolean }
} {
  const rngOverall = seededRandom(dateStr + ':overall')
  const rngStudy = seededRandom(dateStr + ':study')
  const rngFinance = seededRandom(dateStr + ':finance')

  const totalCards = TAROT_CARDS.length // 78

  const overallIdx = Math.floor(rngOverall() * totalCards)
  const overallReversed = rngOverall() < 0.5

  let studyIdx: number
  do {
    studyIdx = Math.floor(rngStudy() * totalCards)
  } while (studyIdx === overallIdx)
  const studyReversed = rngStudy() < 0.5

  let financeIdx: number
  do {
    financeIdx = Math.floor(rngFinance() * totalCards)
  } while (financeIdx === overallIdx || financeIdx === studyIdx)
  const financeReversed = rngFinance() < 0.5

  return {
    overall: { card: TAROT_CARDS[overallIdx], reversed: overallReversed },
    study: { card: TAROT_CARDS[studyIdx], reversed: studyReversed },
    finance: { card: TAROT_CARDS[financeIdx], reversed: financeReversed },
  }
}

// ── CARD IMAGE URLS ───────────────────────────────────────────────────────────

const CARD_IMAGE_BASE = 'https://raw.githubusercontent.com/lambiengcode/flutter-tarot-card/master/images'
const SUIT_PREFIX: Record<Suit, string> = { major: 'm', wands: 'w', cups: 'c', swords: 's', pentacles: 'p' }
const RANK_OFFSET: Partial<Record<Suit, number>> = { wands: 21, cups: 35, swords: 49, pentacles: 63 }

export function getCardImageUrl(card: TarotCard): string {
  const prefix = SUIT_PREFIX[card.suit]
  const rank = card.suit === 'major' ? card.id : card.id - (RANK_OFFSET[card.suit] ?? 0)
  return `${CARD_IMAGE_BASE}/${prefix}${rank.toString().padStart(2, '0')}.jpg`
}

// ── CARD COLOR PALETTE ───────────────────────────────────────────────────────

export function getCardColor(suit: Suit): { bg: string; accent: string; text: string } {
  switch (suit) {
    case 'major':
      return { bg: '#2D1B5E', accent: '#C9A227', text: '#F5F0E8' }
    case 'wands':
      return { bg: '#5C1A0F', accent: '#FF8C42', text: '#FFF3E0' }
    case 'cups':
      return { bg: '#0F2442', accent: '#64B5F6', text: '#E3F2FD' }
    case 'swords':
      return { bg: '#1A2233', accent: '#B8C5D6', text: '#ECEFF1' }
    case 'pentacles':
      return { bg: '#1A3D19', accent: '#C9A227', text: '#F1F8E9' }
  }
}
